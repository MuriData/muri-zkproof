// ─── Helpers ────────────────────────────────────────────────────────────────

// fpStr converts an fp.Element (base field) to its decimal string representation.
func fpStr(e *fp.Element) string {
	var b big.Int
	e.BigInt(&b)
	return b.String()
}

// frStr converts an fr.Element (scalar field) to its decimal string representation.
func frStr(e *fr.Element) string {
	var b big.Int
	e.BigInt(&b)
	return b.String()
}

// pascalCase converts a lowercase circuit name to PascalCase.
// "poi" → "Poi", "keyleak" → "Keyleak", "archive_poi" → "ArchivePoi"
func pascalCase(s string) string {
	parts := strings.Split(s, "_")
	for i, p := range parts {
		if len(p) > 0 {
			parts[i] = strings.ToUpper(p[:1]) + p[1:]
		}
	}
	return strings.Join(parts, "")
}
