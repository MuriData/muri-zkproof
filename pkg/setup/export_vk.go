// ─── PLONK VK Export ────────────────────────────────────────────────────────

// plonkVKData holds template data for PLONK VK Solidity library generation.
type plonkVKData struct {
	LibraryName    string
	DomainSize     uint64
	NbPublicInputs uint64
	Omega          string
	CosetShift     string

	QL, QR, QM, QO, QK g1Point
	S1, S2, S3          g1Point

	// G2 SRS points in EIP-197 order [x1, x0, y1, y0]
	G2Srs0X1, G2Srs0X0, G2Srs0Y1, G2Srs0Y0 string
	G2Srs1X1, G2Srs1X0, G2Srs1Y1, G2Srs1Y0 string
}

// ExportPlonkVKSolidity writes a Solidity library containing only the verification key
// constants for a PLONK circuit, formatted for the PLONK precompile interface.
func ExportPlonkVKSolidity(vk plonk.VerifyingKey, w io.Writer, circuitName string) error {
	concreteVK, ok := vk.(*plonkbn254.VerifyingKey)
	if !ok {
		return fmt.Errorf("expected BN254 PLONK verifying key, got %T", vk)
	}

	data := plonkVKData{
		LibraryName:    pascalCase(circuitName) + "VK",
		DomainSize:     concreteVK.Size,
		NbPublicInputs: concreteVK.NbPublicVariables,
		Omega:          frStr(&concreteVK.Generator),
		CosetShift:     frStr(&concreteVK.CosetShift),

		QL: g1Point{X: fpStr(&concreteVK.Ql.X), Y: fpStr(&concreteVK.Ql.Y)},
		QR: g1Point{X: fpStr(&concreteVK.Qr.X), Y: fpStr(&concreteVK.Qr.Y)},
		QM: g1Point{X: fpStr(&concreteVK.Qm.X), Y: fpStr(&concreteVK.Qm.Y)},
		QO: g1Point{X: fpStr(&concreteVK.Qo.X), Y: fpStr(&concreteVK.Qo.Y)},
		QK: g1Point{X: fpStr(&concreteVK.Qk.X), Y: fpStr(&concreteVK.Qk.Y)},

		S1: g1Point{X: fpStr(&concreteVK.S[0].X), Y: fpStr(&concreteVK.S[0].Y)},
		S2: g1Point{X: fpStr(&concreteVK.S[1].X), Y: fpStr(&concreteVK.S[1].Y)},
		S3: g1Point{X: fpStr(&concreteVK.S[2].X), Y: fpStr(&concreteVK.S[2].Y)},

		// EIP-197 order: [A1, A0, A1, A0] (imaginary first)
		G2Srs0X1: fpStr(&concreteVK.Kzg.G2[0].X.A1), G2Srs0X0: fpStr(&concreteVK.Kzg.G2[0].X.A0),
		G2Srs0Y1: fpStr(&concreteVK.Kzg.G2[0].Y.A1), G2Srs0Y0: fpStr(&concreteVK.Kzg.G2[0].Y.A0),
		G2Srs1X1: fpStr(&concreteVK.Kzg.G2[1].X.A1), G2Srs1X0: fpStr(&concreteVK.Kzg.G2[1].X.A0),
		G2Srs1Y1: fpStr(&concreteVK.Kzg.G2[1].Y.A1), G2Srs1Y0: fpStr(&concreteVK.Kzg.G2[1].Y.A0),
	}

	return plonkVKTemplate.Execute(w, data)
}

