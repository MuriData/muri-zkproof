// ─── Groth16 VK Export ──────────────────────────────────────────────────────

// groth16VKData holds template data for Groth16 VK Solidity library generation.
type groth16VKData struct {
	LibraryName string
	NumInputs   int

	AlphaX, AlphaY string

	BetaNegX1, BetaNegX0, BetaNegY1, BetaNegY0 string
	GammaNegX1, GammaNegX0, GammaNegY1, GammaNegY0 string
	DeltaNegX1, DeltaNegX0, DeltaNegY1, DeltaNegY0 string

	IC []g1Point // IC[0] = constant, IC[1..n] = public input coefficients
}

type g1Point struct {
	X, Y string
}

// ExportGroth16VKSolidity writes a Solidity library containing only the verification key
// constants for a Groth16 circuit, formatted for the Groth16 precompile interface.
func ExportGroth16VKSolidity(vk groth16.VerifyingKey, w io.Writer, circuitName string) error {
	concreteVK, ok := vk.(*groth16bn254.VerifyingKey)
	if !ok {
		return fmt.Errorf("expected BN254 Groth16 verifying key, got %T", vk)
	}

	// Negate Beta, Gamma, Delta G2 points (precompile expects negated form)
	var negBeta, negGamma, negDelta bn254.G2Affine
	negBeta.Neg(&concreteVK.G2.Beta)
	negGamma.Neg(&concreteVK.G2.Gamma)
	negDelta.Neg(&concreteVK.G2.Delta)

	numInputs := len(concreteVK.G1.K) - 1 // K[0] is constant, K[1..n] are public inputs

	data := groth16VKData{
		LibraryName: pascalCase(circuitName) + "VK",
		NumInputs:   numInputs,

		AlphaX: fpStr(&concreteVK.G1.Alpha.X),
		AlphaY: fpStr(&concreteVK.G1.Alpha.Y),

		// EIP-197 order: [X.A1, X.A0, Y.A1, Y.A0] (imaginary first)
		BetaNegX1: fpStr(&negBeta.X.A1), BetaNegX0: fpStr(&negBeta.X.A0),
		BetaNegY1: fpStr(&negBeta.Y.A1), BetaNegY0: fpStr(&negBeta.Y.A0),
		GammaNegX1: fpStr(&negGamma.X.A1), GammaNegX0: fpStr(&negGamma.X.A0),
		GammaNegY1: fpStr(&negGamma.Y.A1), GammaNegY0: fpStr(&negGamma.Y.A0),
		DeltaNegX1: fpStr(&negDelta.X.A1), DeltaNegX0: fpStr(&negDelta.X.A0),
		DeltaNegY1: fpStr(&negDelta.Y.A1), DeltaNegY0: fpStr(&negDelta.Y.A0),
	}

	for _, k := range concreteVK.G1.K {
		data.IC = append(data.IC, g1Point{
			X: fpStr(&k.X),
			Y: fpStr(&k.Y),
		})
	}

	return groth16VKTemplate.Execute(w, data)
}

